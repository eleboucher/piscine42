void	ft_sort_integer_table(int *tab, int size)
{
	int i;
	int j;
	int temp;

	i = size - 1;
	while (i > 0)
	{
		j = 0;
		while (j <= i - 1 )
		{
			if (tab[j + 1] < tab[j])
			{
				temp = tab[j];
				tab[j] = tab[j + 1];
				tab[j + 1] = temp;
			}
			j++;
		}
		i--;
	}
}
