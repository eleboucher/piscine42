#include <stdlib.h>

int	sum(int a, int b)
{
	return (a + b);
}

int	subtract(int a, int b)
{
	return (a - b);
}

int	mul(int a, int b)
{
	return (a * b);
}

int	divi(int a, int b)
{
	return (a / b);
}

int	mod(int a, int b)
{
	return (a % b);
}
